package com.example.tecnotren.entidades;

public class Trenes {

    private int id;
    private String serie;
    private String servicio;
    private String nombre;
    private String traccion;
    private String velocidad;
    private String anchovia;
    private String plazas;
    private String puestaservicio;
    private String unidades;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSerie() {
        return serie;
    }

    public void setSerie(String serie) {
        this.serie = serie;
    }

    public String getServicio() {
        return servicio;
    }

    public void setServicio(String servicio) {
        this.servicio = servicio;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTraccion() {
        return traccion;
    }

    public void setTraccion(String traccion) {
        this.traccion = traccion;
    }

    public String getVelocidad() {
        return velocidad;
    }

    public void setVelocidad(String velocidad) {
        this.velocidad = velocidad;
    }

    public String getAnchovia() {
        return anchovia;
    }

    public void setAnchovia(String anchovia) {
        this.anchovia = anchovia;
    }

    public String getPlazas() {
        return plazas;
    }

    public void setPlazas(String plazas) {
        this.plazas = plazas;
    }

    public String getPuestaservicio() {
        return puestaservicio;
    }

    public void setPuestaservicio(String puestaservicio) {
        this.puestaservicio = puestaservicio;
    }

    public String getUnidades() {
        return unidades;
    }

    public void setUnidades(String unidades) {
        this.unidades = unidades;
    }
}
